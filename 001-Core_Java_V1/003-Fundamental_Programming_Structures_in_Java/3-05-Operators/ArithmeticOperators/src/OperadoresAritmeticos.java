/**
 * 3.5.1 Arithmetic Operators
 * @author kolodani
 */

public class OperadoresAritmeticos {

    /*
     * Tenemos +, -, *, /, %
     * importante, si dividimos dos enteros division entera
     * y necesitamos el modulo para saber el resto
     * no asi si dividimos dos dlotantes
     */

    public static void main(String[] args) {
        
        System.out.println("Suma de 8 + 5 = " + (8 + 5));
        System.out.println("Resta de 8 - 5 = " + (8 - 5));
        System.out.println("Producto de 8 * 5 = " + (8 * 5));
        System.out.println("Division entera 15 / 2 = " + (15 / 2));
        System.out.println("Modulo 2 de 15 = " + (15 % 2));
        System.out.println("Division Flotante de 15.0 / 2.0 = " + (15.0 / 2.0));
    }
}
