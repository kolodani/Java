/**
 * 3.5.5 Combining Assignment with Operators
 * @author kolodani
 */

public class Combinando {

    public static void main(String[] args) {
        int x = 0;
        int y = 0;
        x += 4;
        y = y + 4;
        System.out.println("Valor de X: " + x);
        System.out.println("Valor de Y: " + y);

        /*
         * se puede resumir la operacion de suma y de asignacion en la
         * combinacion del signo +=
         * como tambien la resta
         */
    }
}
